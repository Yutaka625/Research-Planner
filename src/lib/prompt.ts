import type { ResearchPlanInput } from "@/lib/researchPlan";

const sectionTemplate = `
1. ご相談内容
2. 困りごと
3. 心sensor/Affdex SDKが役に立てること
4. 取り組み案
   a. 概要
   b. 場所
   c. 対象者
   d. あるとよい情報
   e. こんなことが言えそう
5. 次のアクション
6. 本資料について
`;

export function buildResearchPlanPrompt(form: ResearchPlanInput) {
  return `
あなたは、心sensor/Affdex SDKを使って出力したデータの活用方法を、お客様と一緒に整理するためのメモを作る支援者です。
以下の入力内容をもとに、お客様にもそのまま見せやすい日本語のMarkdownを作成してください。

書き方の方針:
- 売り込みのように聞こえる言葉、専門用語、業界用語、略語はできるだけ避ける。
- 相手先を指すときは「お客様」と書く。
- 難しい言葉は、できるだけ「小さく試す」「試しに行う」「確認する」と言い換える。
- 「感情を測定する」とは書かない。
- 「心sensor/Affdex SDKを利用して出力した表情感情データを活用する」という前提で書く。
- 表現は「表情の変化から反応の傾向を見やすくする」「時間の流れに沿って見る」「本人の回答を補う」とする。
- 医療診断、心理状態の診断、痛み、ストレス、認知能力の断定はしない。
- 心sensor/Affdex SDKの結果は、アンケート、観察メモ、行動の記録などと合わせて見る補助的な情報として扱う。

入力内容:
- ご相談内容: ${form.research_theme}
- 取り組みの種類: ${form.field}
- 参加する方: ${form.participants}
- 見てみたい場面や内容: ${form.intervention}
- 知りたいこと: ${form.purpose}
- 行う場所: ${form.environment}
- 参加する方への簡単な質問: ${form.self_report_availability}
- まとめ方: ${form.output_format}
- 比べてみたいもの: ${form.comparison_condition || "未定"}
- 一緒に見たい情報: ${form.combined_metrics || "未定"}
- 心sensorの使い方: ${form.sensor_usage || "未定"}
- 確認が必要なルール: ${form.legal_region || "未定"}

構成ルール:
- Markdownで出力する。
- 全体的に、Markdownの表と箇条書きを使い、見出しだけで終わらせず、読み手が次の打ち合わせで使える粒度で整理する。
- 入力内容をそのまま羅列するだけではなく、「何を見るか」「何と比べるか」「どう読むか」「次に何を決めるか」が分かる構造にする。
- 見出しは以下の6セクションに固定する。
${sectionTemplate}
- 「1. ご相談内容」は、入力内容をそのまま繰り返すのではなく、「〇〇における表情感情データの活用について」のようなタイトル調にする。
- 「2. 困りごと」は、必ず「困りごと」「今見えにくいこと」「補助情報として確認したいこと」の3列のMarkdownの表にする。
- 「3. 心sensor/Affdex SDKが役に立てること」は、必ず「見やすくなること」「合わせて見る情報」「注意すること」の3列のMarkdownの表にする。
- 「4. 取り組み案」には、必ず a〜e の小見出しを入れる。
- 「4-a. 概要」には、小さく試す進め方を入れる。例: 5〜10名、2〜3種類の内容、1つの場所など。
- 「4-b. 場所」には、入力された実施場所をもとに、行いやすい場所を書く。
- 「4-c. 対象者」には、参加する方を分かりやすく書く。
- 「4-d. あるとよい情報」には、本人への簡単な質問、観察メモ、行動の記録など、合わせて見られるとよい情報を書く。
- 「4-e. こんなことが言えそう」は、必ず「観点」「見る材料」「根拠として扱える情報」「言えそうなこと」「言い切らないための注意」の5列のMarkdownの表にする。
- 「4-e. こんなことが言えそう」には、少なくとも3行以上入れる。断定ではなく「傾向が見えそう」「比べやすくなりそう」「次回の見直しに使えそう」という表現で書く。
- 「5. 次のアクション」は、必ず「決めること」「確認すること」「準備するもの」の3つの箇条書きに分ける。
- 「5. 次のアクション」には、次回までに決めること、確認すること、準備することを書く。
- 「6. 本資料について」には、以下の趣旨を自然な文章で必ず入れる。
  「この資料は、ヒアリングした範囲の情報をもとに作成した整理案です。記載内容は、結果や効果を保証するものではありません。実施内容や評価方法は、目的、対象者、環境、同意取得、個人情報の取り扱いなどを確認したうえで調整します。」
`;
}
